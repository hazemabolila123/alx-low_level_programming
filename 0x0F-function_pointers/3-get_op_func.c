#include "3-calc.h"

/**
 * op_add - A function that adds 2 numbers
 * @a: First numbet
 * @b: Second number
 *
 * Return: Sum of 2 numbers
 *
 */
int op_add(int a, int b)
{
	return ((a + b));
}

/**
 * op_sub - A function that substracts 2 numbers
 * @a: First numbet
 * @b: Second number
 *
 * Return: Substraction of 2 numbers
 *
 */
int op_sub(int a, int b)
{
	return ((a - b));
}

/**
 * op_mul - A function that multiplys 2 numbers
 * @a: First numbet
 * @b: Second number
 *
 * Return: Multiplication of 2 numbers
 *
 */
int op_mul(int a, int b)
{
	return ((a * b));
}

/**
 * op_div - A function that divides 2 numbers
 * @a: First numbet
 * @b: Second number
 *
 * Return: Division of 2 numbers
 *
 */
int op_div(int a, int b)
{
	return ((a / b));
}

/**
 * op_mod - A function that mods 2 numbers
 * @a: First numbet
 * @b: Second number
 *
 * Return: Remainder
 *
 */
int op_mod(int a, int b)
{
	return ((a % b));
}
